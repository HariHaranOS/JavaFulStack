package uraryoperator;
public class incrementanddecrement
{
	public static void main(String[] args) 
	
	{
		int a=40;
		int b=20;
		int sum=(a+b);
		int difference=(a-b);
		int product=(a*b);
		int quotient=(a/b);
		int remainder=(a%b);
		 
		System.out.println("sum:"+sum);
		System.out.println("difference:"+difference);
		System.out.println("product:"+product);
		System.out.println("quotient:"+quotient);
		System.out.println("remainder:"+remainder);
		System.out.println('\n');
		System.out.println("The value of sum:"+sum);
		System.out.println("increment:"+ ++sum);
		System.out.println("increment:"+ sum++);
		System.out.println("increment:"+ ++sum);	
		System.out.println("decrement:"+sum--);
		System.out.println("decrement:"+--sum);
		System.out.println("decrement:"+sum--);
		System.out.println("The value of difference:"+difference);
		System.out.println("increment:"+ ++difference);
		System.out.println("increment:"+difference++);
		System.out.println("increment:"+ ++difference);
		System.out.println("decrement:"+ --difference);
		System.out.println("decrement:"+ difference--);
		System.out.println("decrement:"+ --difference);
		System.out.println("The value of product:"+product);
		System.out.println("increment:"+ ++product);
		System.out.println("increment:"+ product++);
		System.out.println("increment:"+ ++product);
		System.out.println("decrement:"+ --product);
		System.out.println("decrement:"+ product--);
		System.out.println("decrement:"+ --product);
		System.out.println("The value of quotient:"+quotient);
		System.out.println("increment:"+ ++quotient);
		System.out.println("increment:"+ quotient++);
		System.out.println("increment:"+ ++quotient);
		System.out.println("decrement:"+ --quotient);
		System.out.println("decrement:"+ quotient--);
		System.out.println("decrement:"+ --quotient);
		System.out.println("The value of remaider:"+remainder);
		System.out.println("incremant:"+ ++remainder);
		System.out.println("increment:"+ remainder--);
		System.out.println("increment:"+ ++remainder);
		System.out.println("decrement:"+ --remainder);
		System.out.println("decrement:"+ remainder--);
		System.out.println("decrement:"+ --remainder);
		
		}
	
}
	
	       