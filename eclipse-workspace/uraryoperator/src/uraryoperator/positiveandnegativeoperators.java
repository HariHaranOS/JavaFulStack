package uraryoperator;
public class positiveandnegativeoperators{
	public static void main(String[] args)
	{
		int value=10;
		
		int positivevalue= +value;
		int negativevalue= -value;
		
		System.out.println("original value:"+value);
		System.out.println("positive value:"+positivevalue);
		System.out.println("negative value:"+negativevalue);
		
		
	}
	
}
