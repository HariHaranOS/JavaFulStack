package Switchstatement;

public class Switchexample3 {

	public static void main(String[] args) {
		int number=5;
		
		switch(number) 
		{
		case 1:
			System.out.println("you entered home");
			break;
			
		case 2:
			System.out.println("you entered hostel");
			break;
			
		case 3:
			System.out.println("you entered hotel");
			break;
			
		case 4:
			System.out.println("you entered mall");
			break;
			
		case 5:
			System.out.println("you entered boathouse");
			break;
			
			default:
				System.out.println("you entered jail");
			
				
		}		
			

	}

}
